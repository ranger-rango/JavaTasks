package com.habil.mathlib;

public class MathOperations
{
	public static double add(double numx, double numy)
	{
		return numx + numy;
	}
	public static double sub(double numx, double numy)
	{
		return numx - numy;
	}
	public static double mult(double numx, double numy)
	{
		return numx * numy;
	}
	public static double div(double numx, double numy)
	{
		return numx / numy;
	}
}
